let tipodefigura = prompt ("ingrese numero de lados");

switch (tipodefigura) {
    
    case "0":
    console.log("es un circulo");
    break;

    case "3":
    console.log("es un triangulo");
    break;

    case "4":
    console.log("es un cuadrado");
    break;

    case "5":
    console.log("es un pentagono");
    break;

    case "6":
    console.log("es un hegagono");
    break;

    case ">6":
    console.log("es un poligono");
    break;

    default:
    console.log("el numero ingresado es invalido");









}

